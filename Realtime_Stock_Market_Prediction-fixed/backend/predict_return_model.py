def generate_indicators(df):
    """2. Generate technical indicators: SMA, EMA, RSI, MACD, CCI."""
    df_ta = df.copy()
    
    # Handle NaN values for indicators appropriately by dropping or filling late
    # Ensure there's sufficient data
    
    # SMA (14 days by default)
    sma = SMAIndicator(close=df_ta['Close'], window=14)
    df_ta['SMA'] = sma.sma_indicator()
    
    # EMA (14 days by default)
    ema = EMAIndicator(close=df_ta['Close'], window=14)
    df_ta['EMA'] = ema.ema_indicator()
    
    # RSI (14 days by default)
    rsi = RSIIndicator(close=df_ta['Close'], window=14)
    df_ta['RSI'] = rsi.rsi()
    
    # MACD
    macd = MACD(close=df_ta['Close'])
    df_ta['MACD'] = macd.macd()
    
    # CCI (20 days by default)
    cci_ind = CCIIndicator(high=df_ta['High'], low=df_ta['Low'], close=df_ta['Close'], window=20)
    df_ta['CCI'] = cci_ind.cci()
    
    return df_ta

def calculate_returns(df):
    """3. Calculate next day return: return = (Close[t+1] - Close[t]) / Close[t]."""
    df['Next_Day_Return'] = (df['Close'].shift(-1) - df['Close']) / df['Close']
    return df

def train_and_eval_model(ticker="AAPL"):
    print(f"Fetching data for {ticker}...")
    # df = download_data(ticker)
    df = get_stock_data(ticker, period="5y")
    df = generate_indicators(df)
    df = calculate_returns(df)
    
    # Drop NaNs generated by shifts and rolling windows
    df = df.dropna()
    
    # 4. Use these features as input
    feature_cols = ['Close', 'Volume', 'SMA', 'EMA', 'RSI', 'MACD', 'CCI']
    
    X = df[feature_cols]
    y = df['Next_Day_Return']
    
    # Time series split: 80% train, 20% test
    split_idx = int(len(X) * 0.8)
    X_train, X_test = X.iloc[:split_idx], X.iloc[split_idx:]
    y_train, y_test = y.iloc[:split_idx], y.iloc[split_idx:]
    
    print(f"Training on {len(X_train)} samples, Testing on {len(X_test)} samples.")
    
    # 5. Train a regression model (RandomForestRegressor)
    model = RandomForestRegressor(n_estimators=100, random_state=42, n_jobs=-1)
    model.fit(X_train, y_train)
    
    # 6. Model output should predict return, not price
    predicted_returns = model.predict(X_test)
    
    # 9. Ensure predicted return is constrained between -0.1 and 0.1 to avoid unrealistic prices.
    predicted_returns = np.clip(predicted_returns, -0.1, 0.1)
    
    # Get current prices for the test set periods (Close[t])
    current_prices = X_test['Close'].values
    
    # 7. Convert predicted return to next day price using formula
    predicted_prices = current_prices * (1 + predicted_returns)
    
    # Get actual next day prices (Close[t+1]) calculated using true return
    actual_prices = current_prices * (1 + y_test.values)
    
    # 8. Evaluate using RMSE, MAE, and MAPE.
    rmse_price = np.sqrt(mean_squared_error(actual_prices, predicted_prices))
    mae_price = mean_absolute_error(actual_prices, predicted_prices)
    mape_price = mean_absolute_percentage_error(actual_prices, predicted_prices)
    
    print("\n--- Model Evaluation (Out of Sample) ---")
    print(f"RMSE : {rmse_price:.4f}")
    print(f"MAE  : {mae_price:.4f}")
    print(f"MAPE : {mape_price:.4%}")
    
    # Inference on the most recent row
    latest_row = df.iloc[-1:]
    latest_X = latest_row[feature_cols]
    latest_close = latest_row['Close'].values[0]
    
    next_ret = model.predict(latest_X)[0]
    next_ret = np.clip(next_ret, -0.1, 0.1)
    next_price = latest_close * (1 + next_ret)
    
    print(f"\n--- Latest Prediction for {ticker} ---")
    print(f"Current Date:       {latest_row.index[0].date()}")
    print(f"Current Close:      ${latest_close:.2f}")
    print(f"Predicted Return:   {next_ret:.4%}")
    print(f"Predicted Price:    ${next_price:.2f}")
    
    return model

if __name__ == "__main__":
    train_and_eval_model("AAPL")